    private void silbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silbtnActionPerformed
      if (idtf.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Silinecek ligin ID'sini giriniz.");
    } else {
        try {
            Connection con = Database.getConnection();

            // Silme sorgusu
            String deleteQuery = "DELETE FROM lig WHERE lig_id=?;";

            PreparedStatement deleteStatement = con.prepareStatement(deleteQuery);

            // Silinecek lig'in ID'sini belirle
            int idValue = Integer.parseInt(idtf.getText());
            deleteStatement.setInt(1, idValue);

            int row = deleteStatement.executeUpdate();

            // Silme başarılıysa
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Lig silindi.");
                // Silinen lig'in verilerini temizle
                adtf.setText("");  // Burada adtf'nin lig tablosundaki bir alan olup olmadığına göre değiştirilmelidir.
                ulketf.setText(""); // Benzer şekilde, ulketf'nin lig tablosundaki bir alan olup olmadığına göre değiştirilmelidir.
                taksaytf.setText(""); // Lig sayısı gibi diğer alanları da temizleme işlemleri buraya eklenmelidir.
            } else {
                JOptionPane.showMessageDialog(this, "Lig silinemedi. Belirtilen ID'ye uygun kayıt bulunamadı.");
            }

            con.close();
        } catch (SQLException | NumberFormatException ex) {
            Logger.getLogger(LigGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    }//GEN-LAST:event_silbtnActionPerformed

