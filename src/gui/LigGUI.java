    private void guncellebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncellebtnActionPerformed
          if (idtf.getText().isEmpty() || adtf.getText().isEmpty() || ulketf.getText().isEmpty() || taksaytf.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Eksik bilgi girildi.");
    } else {
        try {
            Connection con = Database.getConnection();

            // Güncelleme sorgusu
            String updateQuery = "UPDATE lig SET lig_adı=?, lig_ulke=?, takim_sayi=? WHERE lig_id=?;";

            PreparedStatement updateStatement = con.prepareStatement(updateQuery);

            // Parametreleri belirle
            updateStatement.setString(1, adtf.getText());
            updateStatement.setString(2, ulketf.getText());
           
            int takimSayi = Integer.parseInt(taksaytf.getText());
            updateStatement.setInt(3, takimSayi);

            // Güncellenecek lig'in ID'sini belirle
            int idValue = Integer.parseInt(idtf.getText());
            updateStatement.setInt(4, idValue);

            int row = updateStatement.executeUpdate();

            // Güncelleme başarılıysa
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Lig güncellendi.");

                // Veritabanından güncellenmiş verileri getir ve textfield'lara yerleştir
                String selectQuery = "SELECT * FROM lig WHERE lig_id=?";
                PreparedStatement selectStatement = con.prepareStatement(selectQuery);
                selectStatement.setInt(1, idValue);
                ResultSet resultSet = selectStatement.executeQuery();

                if (resultSet.next()) {
                    adtf.setText(resultSet.getString("lig_adı"));
                    ulketf.setText(resultSet.getString("lig_ulke"));
                    taksaytf.setText(String.valueOf(resultSet.getInt("takim_sayi")));
                }

            } else {
                JOptionPane.showMessageDialog(this, "Lig güncellenemedi. Belirtilen ID'ye uygun kayıt bulunamadı.");
            }

            con.close();
        } catch (SQLException | NumberFormatException ex) {
            Logger.getLogger(LigGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    }//GEN-LAST:event_guncellebtnActionPerformed

