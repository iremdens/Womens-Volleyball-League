    private void eklebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eklebtnActionPerformed
        if (idtf.getText().isEmpty() || adtf.getText().isEmpty() || takulketf.getText().isEmpty() || kurulustf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Eksik bilgi girildi.");
        } else {
            try {
                con = Database.getConnection();
                PreparedStatement Add = con.prepareCall("insert into takim values (?,?,?,?)");
                int idValue = Integer.parseInt(idtf.getText());
                Add.setInt(1, idValue);
                Add.setString(2, adtf.getText());
                int kurulus = Integer.parseInt(kurulustf.getText());
                Add.setInt(3, kurulus);
                Add.setString(4, takulketf.getText());

                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Takım eklendi.");
                con.close();

            } catch (SQLException ex) {
                Logger.getLogger(OyuncuGUI.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_eklebtnActionPerformed

    private void silbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silbtnActionPerformed
       if (idtf.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Silinecek takımın ID'sini giriniz.");
} else {
    try {
     Connection con = Database.getConnection();
        // Silme sorgusu
        String deleteQuery = "DELETE FROM takim WHERE takim_id=?;";

        PreparedStatement deleteStatement = con.prepareStatement(deleteQuery);

        // Silinecek takımın ID'sini belirle
        int idValue = Integer.parseInt(idtf.getText());
        deleteStatement.setInt(1, idValue);

        int row = deleteStatement.executeUpdate();

        // Silme başarılıysa
        if (row > 0) {
            JOptionPane.showMessageDialog(this, "Takım silindi.");
            // Silinen takımın verilerini temizle
            adtf.setText("");
            kurulustf.setText("");
            takulketf.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Takım silinemedi. Belirtilen ID'ye uygun kayıt bulunamadı.");
        }

        con.close();
    } catch (SQLException | NumberFormatException ex) {
        Logger.getLogger(TakimGUI.class.getName()).log(Level.SEVERE, null, ex);
    }
}

    }//GEN-LAST:event_silbtnActionPerformed

