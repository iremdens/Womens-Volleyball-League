    private void guncellebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncellebtnActionPerformed
        if (idtf.getText().isEmpty() || adtf.getText().isEmpty() || kurulustf.getText().isEmpty() || takulketf.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Eksik bilgi girildi.");
    } else {
        try {
            con = Database.getConnection();

            // Güncelleme sorgusu
            String updateQuery = "UPDATE takim SET takim_ad=?, kurulus_tarih=?, takım_ulke=? WHERE takim_id=?;";

            PreparedStatement updateStatement = con.prepareStatement(updateQuery);

            // Parametreleri belirle
            updateStatement.setString(1, adtf.getText());
           
            int kurulus_tarihi= Integer.parseInt(kurulustf.getText());
            updateStatement.setInt(2, kurulus_tarihi);
            
            updateStatement.setString(3, takulketf.getText());

            // Güncellenecek takımın ID'sini belirle
            int idValue = Integer.parseInt(idtf.getText());
            updateStatement.setInt(4, idValue);

            int row = updateStatement.executeUpdate();

            // Güncelleme başarılıysa
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Takım güncellendi.");

                // Veritabanından güncellenmiş verileri getir ve textfield'lara yerleştir
                String selectQuery = "SELECT * FROM takim WHERE takim_id=?";
                PreparedStatement selectStatement = con.prepareStatement(selectQuery);
                selectStatement.setInt(1, idValue);
                ResultSet resultSet = selectStatement.executeQuery();

                if (resultSet.next()) {
                    adtf.setText(resultSet.getString("takim_ad"));
                    kurulustf.setText(resultSet.getString("kurulus_tarih"));
                    takulketf.setText(resultSet.getString("takım_ulke"));
                }

            } else {
                JOptionPane.showMessageDialog(this, "Takım güncellenemedi. Belirtilen ID'ye uygun kayıt bulunamadı.");
            }

            con.close();
        } catch (SQLException | NumberFormatException ex) {
            Logger.getLogger(TakimGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    }//GEN-LAST:event_guncellebtnActionPerformed

