    private void silbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silbtnActionPerformed
        if (idtf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silinecek oyuncunun ID'sini giriniz.");
        } else {
            try {
                // OyuncuDAO sınıfından bir örnek (instance) oluştur
                OyuncuDAO oyuncuDAO = new OyuncuDAO();

                // ID değerini parçala
                int idValue = Integer.parseInt(idtf.getText());

                // OyuncuDAO sınıfındaki delete metodunu çağır
                int affectedRows = oyuncuDAO.delete(new Oyuncu(idValue));

                // Eğer bir satır etkilendiyse (silme başarılıysa)
                if (affectedRows > 0) {
                    JOptionPane.showMessageDialog(this, "Oyuncu silindi.");
                    // Silinen oyuncunun verilerini temizle
                    adtf.setText("");
                    boytf.setText("");
                    kilotf.setText("");
                    poztf.setText("");
                    dogtf.setText("");
                    miltf.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Oyuncu silinemedi. Belirtilen ID'ye uygun kayıt bulunamadı.");
                }

            } catch (NumberFormatException ex) {
                Logger.getLogger(OyuncuGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_silbtnActionPerformed

    private void eklebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eklebtnActionPerformed
        if (idtf.getText().isEmpty() || adtf.getText().isEmpty() || boytf.getText().isEmpty() || kilotf.getText().isEmpty() || poztf.getText().isEmpty() || miltf.getText().isEmpty() || dogtf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Eksik bilgi girildi.");
        } else {
            try {
                // Oyuncu nesnesi oluştur
                Oyuncu oyuncu = new Oyuncu();
                int idValue = Integer.parseInt(idtf.getText());
                oyuncu.setOyuncu_id(idValue);
                oyuncu.setOyuncu_ad(adtf.getText());
                int boy = Integer.parseInt(boytf.getText());
                oyuncu.setOyuncu_boy(boy);
                int kilo = Integer.parseInt(kilotf.getText());
                oyuncu.setOyuncu_kilo(kilo);
                oyuncu.setOy_pozisyon(poztf.getText());
                oyuncu.setOy_dogumt(dogtf.getText());
                oyuncu.setOy_milliyet(miltf.getText());

                // OyuncuDAO sınıfından bir örnek oluştur
                OyuncuDAO oyuncuDAO = new OyuncuDAO();

                // OyuncuDAO sınıfındaki insert metodunu çağır
                int result = oyuncuDAO.insert(oyuncu);

                // Eğer ekleme başarılıysa
                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Oyuncu eklendi.");
                } else {
                    JOptionPane.showMessageDialog(this, "Oyuncu eklenemedi.");
                }

            } catch (SQLException | NumberFormatException ex) {
                Logger.getLogger(OyuncuGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_eklebtnActionPerformed

