    private void guncellebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncellebtnActionPerformed
        if (idtf.getText().isEmpty() || adtf.getText().isEmpty() || boytf.getText().isEmpty() || kilotf.getText().isEmpty() || poztf.getText().isEmpty() || miltf.getText().isEmpty() || dogtf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Eksik bilgi girildi.");
        } else {
            try {
               Connection con = Database.getConnection();

                // Güncelleme sorgusu
                String updateQuery = "UPDATE oyuncu SET oyuncu_ad=?, oyuncu_boy=?, oyuncu_kilo=?, pozisyon=?, dogum_tarih=?, milliyet=? WHERE oyuncu_id=?;";

                PreparedStatement updateStatement = con.prepareStatement(updateQuery);

                // Parametreleri belirle
                updateStatement.setString(1, adtf.getText());
                int boy = Integer.parseInt(boytf.getText());
                updateStatement.setInt(2, boy);
                int kilo = Integer.parseInt(kilotf.getText());
                updateStatement.setInt(3, kilo);
                updateStatement.setString(4, poztf.getText());
                updateStatement.setString(5, dogtf.getText());
                updateStatement.setString(6, miltf.getText());

                // Güncellenecek oyuncunun ID'sini belirle
                int idValue = Integer.parseInt(idtf.getText());
                updateStatement.setInt(7, idValue);

                int row = updateStatement.executeUpdate();

                // Güncelleme başarılıysa
                if (row > 0) {
                    JOptionPane.showMessageDialog(this, "Oyuncu güncellendi.");

                    // Veritabanından güncellenmiş verileri getir ve textfield'lara yerleştir
                    String selectQuery = "SELECT * FROM oyuncu WHERE oyuncu_id=?";
                    PreparedStatement selectStatement = con.prepareStatement(selectQuery);
                    selectStatement.setInt(1, idValue);
                    ResultSet resultSet = selectStatement.executeQuery();

                    if (resultSet.next()) {
                        adtf.setText(resultSet.getString("oyuncu_ad"));
                        boytf.setText(String.valueOf(resultSet.getInt("oyuncu_boy")));
                        kilotf.setText(String.valueOf(resultSet.getInt("oyuncu_kilo")));
                        poztf.setText(resultSet.getString("pozisyon"));
                        dogtf.setText(resultSet.getString("dogum_tarih"));
                        miltf.setText(resultSet.getString("milliyet"));
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Oyuncu güncellenemedi. Belirtilen ID'ye uygun kayıt bulunamadı.");
                }

                con.close();
            } catch (SQLException | NumberFormatException ex) {
                Logger.getLogger(OyuncuGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_guncellebtnActionPerformed

